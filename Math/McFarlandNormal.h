// ═══════════════════════════════════════════════════════════════════════════
// McFarland Exponential Sampler (internal, for tail sampling)
// ═══════════════════════════════════════════════════════════════════════════

template <typename RNG>
class McFarlandExponential {
public:
    explicit McFarlandExponential(RNG& rng) : rng_(rng) {}

    double operator()() {
        using ET = detail::ExpTables;
        uint64_t u = rng_();
        auto i = static_cast<uint8_t>(u & 0xFF);
        auto s = static_cast<int64_t>(u);
        if (i < ET::N_BINS)
            return ET::X[i] * (s & 0x7FFFFFFFFFFFFFFFLL);

        // Overhang path
        uint8_t j = sampleAlias();
        if (j == 0)
            return ET::X_0 + (*this)();  // Tail: recursive

        return overhang(j);
    }

private:
    RNG& rng_;

    int64_t randInt63() {
        return static_cast<int64_t>(rng_() & 0x7FFFFFFFFFFFFFFFULL);
    }

    uint8_t sampleAlias() {
        using ET = detail::ExpTables;
        uint64_t u = rng_();
        auto j = static_cast<uint8_t>(u & 0xFF);
        auto s = static_cast<int64_t>(u);
        return s >= ET::ipmf[j] ? ET::map[j] : j;
    }

    double overhang(uint8_t j) {
        using ET = detail::ExpTables;
        const double* X_j = ET::X + j;
        constexpr double pow2_63 = 9223372036854775808.0;

        int64_t U_x = randInt63();
        int64_t U_distance = randInt63() - U_x;
        if (U_distance < 0) {
            U_distance = -U_distance;
            U_x -= U_distance;
        }

        double x = (*X_j) * pow2_63 + (X_j[-1] - *X_j) * static_cast<double>(U_x);

        if (U_distance >= ET::iE_max) return x;

        double y_test = ET::Y[j - 1] * pow2_63
                      + (ET::Y[j] - ET::Y[j - 1]) * static_cast<double>(pow2_63 - (U_x + U_distance));
        return y_test <= std::exp(-x) ? x : overhang(j);
    }
};

// ═══════════════════════════════════════════════════════════════════════════
// McFarland Normal Sampler
// ═══════════════════════════════════════════════════════════════════════════

template <typename RNG>
class McFarlandNormal {
public:
    explicit McFarlandNormal(RNG& rng)
        : rng_(rng), exp_(rng) {}

    double operator()() {
        using NT = detail::NormTables;

        uint64_t u = rng_();
        auto i = static_cast<uint8_t>(u & 0xFF);

        // Early exit: ~98.8% of the time (253/256)
        if (i < NT::N_BINS)
            return NT::X[i] * static_cast<int64_t>(u);

        // Overhang path
        return sampleOverhang();
    }

private:
    RNG& rng_;
    McFarlandExponential<RNG> exp_;

    int64_t randInt63() {
        return static_cast<int64_t>(rng_() & 0x7FFFFFFFFFFFFFFFULL);
    }

    uint8_t sampleAlias() {
        using NT = detail::NormTables;
        uint64_t u = rng_();
        auto j = static_cast<uint8_t>(u & 0xFF);
        auto s = static_cast<int64_t>(u);
        return s >= NT::ipmf[j] ? NT::map[j] : j;
    }

    double sampleOverhang() {
        using NT = detail::NormTables;
        constexpr double pow2_63 = 9223372036854775808.0;

        int64_t U_1 = randInt63();
        double sign = (U_1 & 0x100) ? 1.0 : -1.0;
        uint8_t j = sampleAlias();
        const double* X_j = NT::X + j;
        int64_t U_diff;
        double x;

        if (j > NT::j_inflection) {
            // Convex overhang (near peak)
            for (;;) {
                x = (*X_j) * pow2_63 + (X_j[-1] - *X_j) * static_cast<double>(U_1);
                U_diff = randInt63() - U_1;
                if (U_diff >= 0) break;
                if (U_diff >= -NT::max_iE) {
                    double y_test = NT::Y[j - 1] * pow2_63
                                  + (NT::Y[j] - NT::Y[j - 1]) * (pow2_63 - static_cast<double>(U_1 + U_diff));
                    if (y_test < std::exp(-0.5 * x * x)) break;
                }
                U_1 = randInt63();
            }
        } else if (j == 0) {
            // Tail sampling via exponential variates
            do {
                x = (1.0 / NT::X_0) * exp_();
            } while (exp_() < 0.5 * x * x);
            x += NT::X_0;
        } else if (j < NT::j_inflection) {
            // Concave overhang (near tail)
            for (;;) {
                U_diff = randInt63() - U_1;
                if (U_diff < 0) {
                    U_diff = -U_diff;
                    U_1 -= U_diff;
                }
                x = (*X_j) * pow2_63 + (X_j[-1] - *X_j) * static_cast<double>(U_1);
                if (U_diff > NT::min_iE) break;
                double y_test = NT::Y[j - 1] * pow2_63
                              + (NT::Y[j] - NT::Y[j - 1]) * (pow2_63 - static_cast<double>(U_1 + U_diff));
                if (y_test < std::exp(-0.5 * x * x)) break;
                U_1 = randInt63();
            }
        } else {
            // Inflection point: full rectangle rejection
            for (;;) {
                x = (*X_j) * pow2_63 + (X_j[-1] - *X_j) * static_cast<double>(U_1);
                double y_test = NT::Y[j - 1] * pow2_63
                              + (NT::Y[j] - NT::Y[j - 1]) * static_cast<double>(randInt63());
                if (y_test < std::exp(-0.5 * x * x)) break;
                U_1 = randInt63();
            }
        }
        return sign * x;
    }
};

